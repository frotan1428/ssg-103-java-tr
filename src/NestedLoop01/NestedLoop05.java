package NestedLoop01;

public class NestedLoop05 {

    public static void main(String[] args) {

       // soru 6)  Asagidaki sekil cizen kodu yaziniz

        /*
         * * * * *
         * * * * *
         * * * * *
         * * * * *
         * * * * *
         */

        int input=5;

        for (int i = 1; i <=input ; i++) {

            for (int j = 1; j <=input ; j++) {

                System.out.print("* ");
            }
            System.out.println("");
        }
    }
}
